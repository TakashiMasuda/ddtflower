/*
 * 関数名:createTableCourseGuide
 * 引数  :var courseNumber
 * 戻り値:なし
 * 概要  :コース案内のテーブルを作る。
 　　　　　　　引数でどのページについての案内かを選択する
 　　　　　　　０：プリザ　　１：生花　　２：NFD　　３：FC　　の中から選ぶ
 * 作成日:2015.02.06
 * 作成者:T.Y
 */
function createTableCourseGuide (courseNumber) {
  // jqueryの記述の始まり
    $(function() {

        // sectionタグの最初にtableタグを作る
        $('section').prepend($('<table></table>')
                        // テーブルにcourseGuideクラスをつける
                        .addClass('courseGuide')
                        // テーブルの概要を表すcaptionタグを作る
                        .prepend($('<caption></caption>')
                            // captionタグにテーブルの説明文を入れる
                            .text('コース案内')
                        )
                        // ループでthとtdに値を入れる
                    );
        // テーブルの最後にレコードをループで作る
        for (var i = 0; i < CourseGuideTh[0].length; i++) {
            // テーブルのtrを作る
            $('table').append($('<tr></tr>')
                // テーブルのthを作る
                .prepend('<th></th>') 
            );
        }
        // テーブルのtdを作る
        $('.courseGuide th').after('<td></td>');
        // テーブルのtdを作る
        $('.courseGuide th').after('<td></td>');
                     

        // ループでテーブルのthとtdに値を入れる
        for (var x = 0; x < CourseGuideTd[0].length; x++) {
            //カウンタ変数が３より低い時の処理(thは3個しかないからthについてのループは3で止まる)
            if(x < CourseGuideTh[0].length) {
                // テーブルのthに配列値を入れる
                $('.courseGuide tr:eq('+ x +') th:eq(0)').html(CourseGuideTh[courseNumber][x]);    
            }
            // テーブルのtdに配列から値を入れる
            $('.courseGuide td:eq('+ x +')').html(CourseGuideTd[courseNumber][x]);
        }

    });// jqueryの記述の終わり
}

