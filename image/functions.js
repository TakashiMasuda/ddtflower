/*
 * 関数名:creatScrollPictures
 * 引数  :なし
 * 戻り値:なし
 * 概要  :写真のスクロールを作る
 * 作成日:2015.02.08
 * 作成者:T.Y
 */
function createScrollPictures () {
  // jqueryの記述の始まり
$(function() {
    // フッター前のカルーセルのギャラリーに画像を追加していくfor文
        for(var i = 0; i < 10; i++){
            // カルーセルのギャラリーのタグに対し、要素を追加していく。最上位はdivタグとなる。
            $('.foot_gallery').append($('<div></div>')
                                    // その子要素としてaタグを追加する。
                                    .append($('<a></a>')
                                        // 属性を追加する。
                                        .attr({
                                            // href属性に画像のURLを設定する。
                                            href: 'image/' + i + '.JPG',
                                            // galleryという名前で画像をグループ化する。
                                            rel:"gallery"
                                        })
                                        // 画像のタグを最下位に置く。
                                        .append($('<img>')
                                                // 画像のパスを設定する。
                                                .attr('src', 'image/' + i + '.JPG')
                                                )
                                    )
                                );
        }
       
        // jQueryプラグイン「Slick」によりフッター前にカルーセルのギャラリーを作成する。
        $('.foot_gallery').slick({
            // 矢印ボタンでの制御を有効にする。
            accessibility:true,
            // 矢印ボタンを使う。
            arrows:true,
            // レスポンシブレイアウトに対応する。
            responsive:true,
            // 一度に3個の画像をギャラリーに並べる。
            slidesToShow:3,
            // 緩急をつけたアニメーションでスクロールする。
            easing:'swing',
            // 画像を中心に配置する。
            centerMode: true
            });
        
        // フッター前のギャラリーをクリックしたらjQueryプラグイン「fancybox」により
        // 拡大表示を行うようにする。
        $(".foot_gallery a").fancybox({
            'hideOnContentClick': true
        }); 
    });// jqueryの記述の終わり
}

